// If statement
if (x > 10) {
    y = 20;
}

// If-else statement
if (score >= 50) {
    printf("Pass\n");
} else {
    printf("Fail\n");
}

// If-else-if statement
if (grade == 'A') {
    bonus = 100;
} else if (grade == 'B') {
    bonus = 50;
} else {
    bonus = 0;
}

// For loop
for (i = 0; i < 10; i++) {
    printf("%d\n", i);
}

// While loop
while (n > 0) {
    sum += n;
    n--;
}

// Switch case
switch (option) {
    case 1:
        printf("Option 1\n");
        break;
    case 2:
        printf("Option 2\n");
        break;
    default:
        printf("Invalid option\n");
}